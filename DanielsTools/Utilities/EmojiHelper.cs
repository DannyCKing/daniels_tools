        public static List<Emoji> ParseEmojis()
        {
            List<Emoji> returnValues = new List<Emoji>();
            var list = EMOJIS_STRING.Split(Environment.NewLine);
            foreach (var line in list)
            {
                if (string.IsNullOrWhiteSpace(line))
                {
                    continue;
                }
                var items = line.Split(' ');
                var id = int.Parse(items[0]);
                var cost = int.Parse(items[1]);
                var code = HttpUtility.HtmlDecode(items[2]);
                var description = items[3];
                var emoji = new Emoji { Id = id, Code = code + ";", Cost = cost, Description = description };
                returnValues.Add(emoji);
            }

            return returnValues;
        }

